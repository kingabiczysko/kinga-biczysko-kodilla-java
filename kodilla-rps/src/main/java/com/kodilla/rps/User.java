package com.kodilla.rps;

public class User {
    String user;
    int roundNumber;

    public User() {
        this.user = user;
    }

    public String getUser() {
        return user;
    }

    public int getRoundNumber() {
        return roundNumber;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public void setRoundNumber(int roundNumber) {
        this.roundNumber = roundNumber;
    }


}
